// Close the dropdown menu if the user clicks outside of it
window.onclick = function(event) {
  if (!event.target.matches('.dropbtn')) {
    var dropdowns = document.getElementsByClassName("dropdown-content");
    var i;
    for (i = 0; i < dropdowns.length; i++) {
      var openDropdown = dropdowns[i];
      if (openDropdown.classList.contains('show')) {
        openDropdown.classList.remove('show');
      }
    }
  }
}

function checkSigned(){
  var user= localStorage.getItem('user')
  if (user == null) return
  user = JSON.parse(user)
  var img = user.avatar.image
  
  var loginAvatar = document.getElementById('signined')
  loginAvatar.setAttribute('src' , 'data:image/png;base64,'+ img )
  var loginIcon = document.getElementById('login').style.display = 'none'
  loginAvatar.style.display = 'inline'
  var header_right = document.getElementById('header_right')
  header_right.style.top = '-49px'
}

function signOut(){
  localStorage.removeItem('user')
  var loginAvatar = document.getElementById('signined')
  loginAvatar.style.display ='none'
  document.getElementById('header_right').style.top = '-43px'
  
  var loginIcon = document.getElementById('login').style.display = 'inline'
}